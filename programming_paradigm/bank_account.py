

class BankAccount:
    def __init__(self,account_balance):
        self.account_balance = 0
    
    def deposit(amount):
        account_balance +=amount

    def withdraw(amount):
        if amount > account_balance:
            account_balance -=amount 
            return True
        else:
            return False






    def display_balance():
        print(f"Current Balance: ${account_balance}")